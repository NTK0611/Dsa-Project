    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(ID.getText().equals("")||PhoneNumber.getText().equals("")||Address.getText().equals("")||
                Gender.getText().equals("")||Age.getText().equals("")||Name.getText().equals("")){
            JOptionPane.showMessageDialog(null,"All Field Required");
        }
        else if (checkIDC())
     {
        JOptionPane.showMessageDialog(null,"ID or Contact already exists");
    }
        else
        {
            try{
            
            Patient patient = new Patient();
            patient.setId(Integer.parseInt(ID.getText()));
                 patient.setName(Name.getText());
                 patient.setAge(Integer.parseInt(Age.getText()));
                 patient.setGender(Gender.getText());
                 patient.setAddress(Address.getText());
                 patient.setContact(PhoneNumber.getText());
                 allPatients.add(patient);
                 
                 ID.setText("");
                 Name.setText("");
                 Address.setText("");
                 PhoneNumber.setText("");
                 Age.setText("");
                 Gender.setText("");
                 writeAllData();
                 saveAllData();
            } catch(Exception e){
                JOptionPane.showMessageDialog(null,"ID and Age must be Integer");
            }
                 
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
        if(ID.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please enter Valid Id for search !!");
        }
        else 
        {
            Patient patient = null;
            for(int i=0; i< allPatients.size();i++)
            {
                if(ID.getText().equals(allPatients.get(i).getId()+"")){
                    patient = allPatients.get(i);
                    break;
                }
            }
            if(patient == null)
            {
                JOptionPane.showMessageDialog(null,"Please enter Valid Id for search !!");
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Patient Info \n"+patient.getName()+"\nContact = "+patient.getContact()+"\nAddress = "+patient.getAddress());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

