       void saveAllData()
       {
           try{
               FileWriter fileWriter = new FileWriter("PData.txt");
               for(int i=0;i<allPatients.size();i++)
               {
                   fileWriter.write(allPatients.get(i).getId()+";"+allPatients.get(i).getName()+";"+allPatients.get(i).getAge()+";"
                           +allPatients.get(i).getGender()+";"+allPatients.get(i).getAddress()+";"+allPatients.get(i).getContact()+"\n");
               }
               fileWriter.close();
               
           } catch(Exception e){
               
           }
       }
    
    boolean checkIDC()
        {
            for(int i = 0; i< allPatients.size();i++)
            {
                if(ID.getText().equals(allPatients.get(i).getId()+"")||PhoneNumber.getText().equals(allPatients.get(i).getId()+""))
                {
                    return true;
                }
            }
            
            
            
            return false;
        }
    
